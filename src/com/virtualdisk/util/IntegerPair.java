package com.virtualdisk.util;

public class IntegerPair
{
    protected Integer first;
    protected Integer second;

    public IntegerPair(Integer first, Integer second)
    {
        this.first = first;
        this.second = second;
    }

    public Integer first()
    {
        return first;
    }

    public Integer second()
    {
        return second;
    }
}

